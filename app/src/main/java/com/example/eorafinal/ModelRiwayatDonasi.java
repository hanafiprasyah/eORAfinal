package com.example.eorafinal;

class ModelRiwayatDonasi {
    private String nama_donatur;
    private String jumlah_donasi;
    private String jenis_donasi;

    public ModelRiwayatDonasi() {
        this.nama_donatur = nama_donatur;
        this.jumlah_donasi = jumlah_donasi;
        this.jenis_donasi = jenis_donasi;
    }

    public String getNama_donatur() {
        return nama_donatur;
    }

    public void setNama_donatur(String nama_donatur) {
        this.nama_donatur = nama_donatur;
    }

    public String getJumlah_donasi() {
        return jumlah_donasi;
    }

    public void setJumlah_donasi(String jumlah_donasi) {
        this.jumlah_donasi = jumlah_donasi;
    }

    public String getJenis_donasi() {
        return jenis_donasi;
    }

    public void setJenis_donasi(String jenis_donasi) {
        this.jenis_donasi = jenis_donasi;
    }

}
